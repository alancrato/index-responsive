                                    Bursite é a inflamação das bursas do Ombro, bolsas que recobrem os tendões do ombro. Não é uma doença em si, mas a expressão de diversas doenças ou lesões do ombro, que geram aumento de líquido da bursa. É comum a descrição de bursites em exames de imagem, em boa parte dos casos é um achado leve, sem grande significado clínico. Em outros casos, vem acompanhada de lesões mais importantes, que afetam a estrutura dos tendões do ombro (tendões do manguito rotador).

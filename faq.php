                                    A luxação ou deslocamento do ombro pode ocorrer uma única vez ou repetidamente. Ombros que sofreram mais de um episódio de deslocamento (luxação recidivante) necessitam na maioria das vezes de tratamento cirúrgico, devendo ser procurado ortopedista com atuação em cirurgia do ombro. Nos casos onde houve o ombro saiu a primeira vez, pode permitir o tratamento não-cirúrgico com trabalho muscular voltado para a estabilização do ombro. Pacientes acima de 40 anos que sofreram luxação precisam de uma atenção especial, pelo risco adicional de lesão dos tendões, mesmo após um único episódio.

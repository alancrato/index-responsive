                                    O tratamento de lesões ou rupturas dos tendões do manguito rotador (tendões que recobrem o ombro) depende da avaliação da lesão em si, do grau de atividade e saúde geral do paciente (se é ativo, se não sofre de doenças graves etc) e dos sintomas dos quais sofre o cliente.<br/>
                                    Lesões que não comprometem a espessura total do tendão (lesões parciais) via de regra permitem um tratamento não-cirúrgico. Devem entretanto ser acompanhadas periodicamente para observar sua estabilização ou evolução.
